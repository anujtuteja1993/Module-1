function Sum (a,b)
{
    return a+b;
}


function Subtract (a,b)
{
    return a-b;
}


function Multiply (a,b)
{
    return a*b;
}


function Divide (a,b)
{
    return a/b;
}

function concatName(name)
{
    return `Hello ${name}`;
}


